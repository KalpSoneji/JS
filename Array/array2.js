var arr = [1, 5, 10, 20, 102];

console.log(arr);

console.log("Length of array: ", arr.length);

console.log("Pushing 201 into array at end: ", arr.push(201), arr);

console.log("Popping 201 from array from end : ", arr.pop(), arr);

console.log("Adding 333 at starting of the array: ", arr.unshift(333), arr);

console.log("Removing 333 from starting of the array: ", arr.shift(), arr);

console.log("Sorting array: ", arr.sort(), arr);

console.log("Converting array into a string: ", arr.join(), arr);

